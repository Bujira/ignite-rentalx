describe("Create category", () => {
  it("Should be able to create a new car category", () => {
    const sum = 2 + 2;
    const result = 4;

    expect(sum).toBe(result);
  });

  it("Should not be able to create an existing car category", () => {
    const sum = 2 + 2;
    const result = 5;

    expect(sum).not.toBe(result);
  });
});
